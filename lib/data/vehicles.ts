/** All makes, alphabetically sorted, for the make picker. */
export const ALL_MAKES = Object.keys(VEHICLE_MAKES).sort((a, b) =>
  a.localeCompare(b),
);

/** Models for a given make (empty if the make is unknown / custom). */
export function modelsForMake(make: string): string[] {
  const key = Object.keys(VEHICLE_MAKES).find(
    (m) => m.toLowerCase() === make.trim().toLowerCase(),
  );
  return key ? VEHICLE_MAKES[key] : [];
}

